// add function
function add (num1, num2){
    return num1 + num2;
}

// subtract function
function subtract (num1, num2){
    return num1 - num2;
}

// multiply function
function multiply (num1, num2){
    return num1 * num2;
}

// divide function
function divide (num1, num2){
    return num1 / num2;
}

console.log(add(2,4));
console.log(subtract(5,2));
console.log(multiply(4,8));
console.log(divide(30,5));
